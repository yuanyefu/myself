package com.docker.yyf.entity;

public class RecordTotal {

    public Integer total;

    public Integer addNumber;

    public Integer reduceNumber;

    public Integer getTotal() {
        return total;
    }

    public void setTotal(Integer total) {
        this.total = total;
    }

    public Integer getAddNumber() {
        return addNumber;
    }

    public void setAddNumber(Integer addNumber) {
        this.addNumber = addNumber;
    }

    public Integer getReduceNumber() {
        return reduceNumber;
    }

    public void setReduceNumber(Integer reduceNumber) {
        this.reduceNumber = reduceNumber;
    }
}
